//Escreva um programa que pergunte a quantidade de km percorridos por um carro alugado pelo usuário,
// assim como a quantidade de dias pelos quais o carro foi alugado. 
//Calcule o preço a pagar, sabendo que o carro custa 60,00 reais por dia e 0,15 centavos por km rodado.


let distanciaPercorridaPeloCarro = 200

let quantidadeDiasCarroDisponivel = 7

let totalValorCarroDiasDisponiveis = (60 * quantidadeDiasCarroDisponivel) + (0.15 * distanciaPercorridaPeloCarro)

console.log(`O valor total a pagar pelo carro alugado, levando em consideração a quantidade de dias pelos quais o carro foi alugado e quantidade de km percorridos é de:${totalValorCarroDiasDisponiveis} reais`) // 450 reais


